package com.fitj.classes;

import java.util.*;

/**
 * Classe qui représente une commande.
 * Une commande est un ensemble de produits achetés par un client à un coach.
 */
public abstract class Commande {

    /**
     * Default constructor
     */
    public Commande() {
    }

    /**
     * L'identifiant de la commande.
     */
    private int id;

}